#this is a great way to create a loop without a list
for x in range(10):
    print("Ducks are cute")

#for giving a range through specific numbers
for x in range(5, 12):
    print(x)

#for counting in certain increments
for x in range(5, 12, 5):
    print(x)

#a while loops as long as a condition is true
buttcrack = 5

while buttcrack <10:
    print(buttcrack)
    #to add 1 to a variable each time during a condition
    buttcrack += 1

